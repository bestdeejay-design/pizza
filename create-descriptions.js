let addedCount = 0;
let notFoundCount = 0;
const notFoundProducts = [];

// Проходим по всем категориям
Object.keys(menuData.menu).forEach(category => {
  console.log(`\n📦 Категория: ${category}`);
  
  const categoryDesc = descriptionsByCategory[category];
  
  menuData.menu[category].forEach(product => {
    if (!product.description || product.description.trim() === '') {
      let description = '';
      
      // Ищем индивидуальное описание
      if (categoryDesc && categoryDesc.items) {
        for (const [itemName, itemDesc] of Object.entries(categoryDesc.items)) {
          if (product.title.toLowerCase().includes(itemName.toLowerCase())) {
            description = itemDesc;
            break;
          }
        }
      }
      
      // Если не нашли индивидуальное, используем базовое
      if (!description && categoryDesc && categoryDesc.base) {
        description = categoryDesc.base;
      }
      
      // Если всё ещё нет, ставим заглушку
      if (!description) {
        description = 'Вкусный продукт от нашей пиццерии.';
        notFoundCount++;
        if (notFoundProducts.length < 10) {
          notFoundProducts.push(product.title);
        }
      } else {
        addedCount++;
      }
      
      product.description = description;
      
      if (addedCount <= 5) {
        console.log(`  ✅ "${product.title}" → добавлено описание`);
      }
    }
  });
});

