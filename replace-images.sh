#!/bin/bash
# 🚀 Скрипт замены изображений на оптимизированные
# Оригиналы сохраняются в img-old/

set -e  # Остановиться при ошибке

echo "======================================================================"
echo "🔄 Замена изображений на оптимизированные (WebP)"
echo "======================================================================"
echo ""

# Проверка наличия папок
if [ ! -d "img-optimized" ]; then
    echo "❌ Папка img-optimized/ не найдена!"
    exit 1
fi

if [ ! -d "img" ]; then
    echo "❌ Папка img/ не найдена!"
    exit 1
fi

# Создаем резервную копию текущих изображений
echo "📦 Создание резервной копии текущих изображений..."
if [ -d "img-backup" ]; then
    echo "⚠️  Папка img-backup/ уже существует, удаляю..."
    rm -rf img-backup
fi

mv img img-backup
echo "✅ Текущие изображения перемещены в img-backup/"

# Переименовываем оптимизированную папку
mv img-optimized img
echo "✅ Оптимизированные изображения перемещены в img/"

echo ""
echo "======================================================================"
echo "✅ ГОТОВО!"
echo "======================================================================"
echo ""
echo "📁 Новая структура:"
echo "   img/ - оптимизированные WebP (49MB)"
echo "   img-old/ - оригиналы JPG/PNG (92MB) из optimize-images.py"
echo "   img-backup/ - предыдущая версия img (резервная копия)"
echo ""
echo "💡 Рекомендации:"
echo "   1. Проверьте работу сайта с новыми изображениями"
echo "   2. Если всё хорошо - можно удалить img-backup/ и img-old/"
echo "   3. Обновите HTML если нужно (расширение .webp)"
echo ""
echo "🔍 Команды для проверки:"
echo "   du -sh img/ img-backup/ img-old/"
echo "   ls img/ | head -5"
echo ""
